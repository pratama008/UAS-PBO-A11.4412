    private void tKamarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tKamarMouseClicked
        JTable source = (JTable)evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        this.kode = source.getModel().getValueAt(row, 0)+"";
        tfKode.setText(source.getModel().getValueAt(row, 0)+"");
        tfNomer.setText(source.getModel().getValueAt(row, 1)+"");
        cbJenis.setSelectedItem(source.getModel().getValueAt(row, 2)+"");
        sHarga.setValue(Integer.valueOf(source.getModel().getValueAt(row, 3)+""));
    }//GEN-LAST:event_tKamarMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            PreparedStatement ps = conn.prepareStatement("INSERT INTO kamar VALUES (?, ?, ?, ?)");
            ps.setString(1, tfKode.getText());
            ps.setString(2, tfNomer.getText());
            ps.setString(3, cbJenis.getSelectedItem().toString());
            ps.setString(4, sHarga.getValue().toString());
            ps.execute();
            
            clear();
        }catch(SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

