    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.kode == null) {
            JOptionPane.showMessageDialog(null, "Pilih Data Terlebih Dahulu!");
            return;
        }
        try{
            PreparedStatement ps = conn.prepareStatement("UPDATE kamar SET kode_kamar = ?, nomer_kamar = ?, jenis_kamar = ?, harga_jual = ? WHERE kode_kamar = ?");
            ps.setString(1, tfKode.getText());
            ps.setString(2, tfNomer.getText());
            ps.setString(3, cbJenis.getSelectedItem()+"");
            ps.setString(4, sHarga.getValue()+"");
            ps.setString(5, this.kode);
            ps.execute();
            
            clear();
        }catch(SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

