    private void clear() {
        this.kode = null;
        tfKode.setText("");
        tfNomer.setText("");
        cbJenis.setSelectedIndex(0);
        sHarga.setValue(0);
        setTableData();
    }

    private void setTableData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try{
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM kamar");
            ResultSet rs = ps.executeQuery();
            Object[] obj = new Object[4];
            while(rs.next()) {
                obj[0] = rs.getString("kode_kamar");
                obj[1] = rs.getString("nomer_kamar");
                obj[2] = rs.getString("jenis_kamar");
                obj[3] = rs.getString("harga_jual");
                
                model.addRow(obj);
            }
        }catch(SQLException e) {
            e.printStackTrace();
        }
    }
    
